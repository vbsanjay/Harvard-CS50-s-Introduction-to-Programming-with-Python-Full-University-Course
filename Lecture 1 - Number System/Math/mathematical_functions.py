print(abs(-5))    # Output: 5
print(abs(3.14))  # Output: 3.14

print(round(3.14159))      # Output: 3
print(round(3.14159, 2))   # Output: 3.14
print(round(10 / 3))       # Output: 3
print(round(10 / 3, 2))    # Output: 3.33

print(max(1, 5, 3, 9, 2))          # Output: 9
print(max([1, 5, 3, 9, 2]))        # Output: 9
print(max("apple", "banana"))      # Output: 'banana'

print(min(1, 5, 3, 9, 2))          # Output: 1
print(min([1, 5, 3, 9, 2]))        # Output: 1
print(min("apple", "banana"))      # Output: 'apple'

print(pow(2, 3))         # Output: 8 (2^3)
print(pow(2, 3, 5))      # Output: 3 (2^3 % 5)